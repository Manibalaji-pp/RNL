const cleanName = (name: string): string => {
  return name
    .replace(/_ICU_ICU|_NICU_NICU|_ICU|_NICU/g, '')
    .replace(/_Nursing_Pod\(ICU\)|_Nursing_Pod\(NICU\)/g, ' Nursing Pod')
    .replace(/_/g, ' ')
    .replace(/\[\d+\]|\[\w\]/g, '') // Removes things like [2], [B]
    .trim();
};

